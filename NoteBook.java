package education;
public class NoteBook {
    String name;
    int OZU;
    int HDD;
    String OS;
    String color;

    public NoteBook(String name, int OZU, int HDD, String OS, String color){
        this.name = name;
        this.OZU = OZU;
        this.HDD = HDD;
        this.OS = OS;
        this.color = color;
    }

}